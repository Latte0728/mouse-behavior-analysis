        ax1.set_title('2. 행동 지표 비교 (개체 vs 대조군)', fontsize=12, fontweight='bold', loc='left', color='#1e293b')
        ax1.legend(fontsize=8, frameon=False)
        for sp in ['top', 'right']:
            ax1.spines[sp].set_visible(False)

        # 3. 궤적 / 4. 히트맵
        if coords is not None and not coords.empty:
            ax2 = fig.add_axes([0.08, 0.10, 0.40, 0.30])
            ax2.scatter(coords['x'], coords['y'], c=coords['frame'], cmap='viridis', s=2, alpha=0.6)
