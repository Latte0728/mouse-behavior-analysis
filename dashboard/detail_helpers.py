    buf = io.BytesIO()
    with PdfPages(buf) as pdf:
        fig = plt.figure(figsize=(8.27, 11.69))  # A4
        fig.subplots_adjust(left=0.08, right=0.95, top=0.94, bottom=0.06, hspace=0.55)

