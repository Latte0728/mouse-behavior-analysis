def build_comparison_pptx(cfg, result, disp, lines, comp_type="group"):
    """비교 분석 결과를 PPTX로 생성 (프레젠테이션 스타일)"""
    from pptx import Presentation
    from pptx.util import Inches, Pt
    from pptx.dml.color import RGBColor
    import io
    import pandas as pd
    from datetime import datetime
    
    prs = Presentation()
    prs.slide_width = Inches(13.333)
    prs.slide_height = Inches(7.5)
    
    c_dark = RGBColor(15, 23, 42)
    c_blue = RGBColor(37, 99, 235)
    c_gray = RGBColor(100, 116, 139)
    c_dark_text = RGBColor(30, 41, 59)

    # ── Slide 1: 표지 ──
    slide = prs.slides.add_slide(prs.slide_layouts[6])
    fill = slide.background.fill
    fill.solid()
    fill.fore_color.rgb = c_dark
    
    txBox = slide.shapes.add_textbox(Inches(1.0), Inches(2.2), Inches(11.3), Inches(3.0))
    tf = txBox.text_frame
    tf.word_wrap = True
    p = tf.paragraphs[0]
    p.text = cfg.get("title", "비교 분석 프레젠테이션")
    p.font.size = Pt(40)
    p.font.bold = True
    p.font.color.rgb = RGBColor(255, 255, 255)
    
    p2 = tf.add_paragraph()
    p2.text = f"발행일: {datetime.now().strftime('%Y-%m-%d %H:%M')}  |  통계 분석 보고서"
    p2.font.size = Pt(16)
    p2.font.color.rgb = c_gray
    p2.space_before = Pt(20)

    # ── Slide 2: 분석 개요 ──
    slide = prs.slides.add_slide(prs.slide_layouts[6])
    txBox = slide.shapes.add_textbox(Inches(1.0), Inches(0.6), Inches(11.3), Inches(1.0))
    p = txBox.text_frame.paragraphs[0]
    p.text = "1. 분석 개요 및 실험 디자인"
    p.font.size = Pt(24)
    p.font.bold = True
    p.font.color.rgb = c_blue
    
    txBox2 = slide.shapes.add_textbox(Inches(1.0), Inches(1.8), Inches(11.3), Inches(4.5))
    tf2 = txBox2.text_frame
    tf2.word_wrap = True
    
    def add_bullet(tf, title, desc):
        p = tf.add_paragraph() if tf.paragraphs[0].text else tf.paragraphs[0]
        p.text = "• " + title
        p.font.size = Pt(18)
        p.font.bold = True
        p.font.color.rgb = c_dark_text
        p.space_after = Pt(6)
        
        p_desc = tf.add_paragraph()
        p_desc.text = "  " + desc
        p_desc.font.size = Pt(15)
        p_desc.font.color.rgb = c_gray
        p_desc.space_after = Pt(24)
        
    if comp_type == "group":
        add_bullet(tf2, "실험 목적", "약물(Yohimbine) 투여 조건별 5종 행동 스코어 변화 패턴을 통계적으로 비교 분석합니다.")
        add_bullet(tf2, "비교 그룹", f"대조군 Dose {cfg.get('base_dose', 0)} ({result['meta']['n_base']}마리) vs 실험군 Dose {', '.join(str(d) for d in cfg.get('comp_doses', [6]))} ({result['meta']['n_comp']}마리)")
        add_bullet(tf2, "적용 통계 모델", "선형 혼합 효과 모델(LME)을 통한 개체 간의 무작위 효과 통제 및 고정 효과(약물 효과) 검증.")
    elif comp_type == "individual":
        add_bullet(tf2, "실험 목적", "동일 조건 내 서로 다른 두 개체(개체 A vs 개체 B)의 행동 패턴 차이를 통계적으로 분석합니다.")
        add_bullet(tf2, "비교 대상", f"개체 A: {cfg.get('a_animal', '')} vs 개체 B: {cfg.get('b_animal', '')}")
        add_bullet(tf2, "적용 통계 모델", "독립표본 t-검정(Student's t-test)을 통한 지표별 평균 차이 유의성 검정.")
    else: # time
        add_bullet(tf2, "실험 목적", "동일 개체의 실험 시간대 경과에 따른 행동 스코어 패턴의 변화를 종단적으로 분석합니다.")
        add_bullet(tf2, "분석 대상", f"마우스 ID: {cfg.get('animal', '')} (총 {cfg.get('n_win', 3)}개 시간 구간 분할)")
        add_bullet(tf2, "적용 통계 모델", "시간 구간별 독립표본 t-검정 및 첫/끝 구간 평균 변화율 검증.")

    # ── Slide 3: 통계 분석 요약 (표) ──
    slide = prs.slides.add_slide(prs.slide_layouts[6])
    txBox = slide.shapes.add_textbox(Inches(1.0), Inches(0.6), Inches(11.3), Inches(1.0))
    p = txBox.text_frame.paragraphs[0]
    p.text = "2. 행동 지표별 비교 분석 요약"
    p.font.size = Pt(24)
    p.font.bold = True
    p.font.color.rgb = c_blue
    
    rows, cols = len(disp) + 1, len(disp.columns)
    table_shape = slide.shapes.add_table(rows, cols, Inches(1.0), Inches(1.6), Inches(11.3), Inches(4.5))
    table = table_shape.table
    
    col_names = [col.replace("개체 A", "Animal A").replace("개체 B", "Animal B") for col in disp.columns]
    for j, name in enumerate(col_names):
        cell = table.cell(0, j)
        cell.text = name
        cell.fill.solid()
        cell.fill.fore_color.rgb = RGBColor(226, 232, 240)
        for p_t in cell.text_frame.paragraphs:
            p_t.font.bold = True
            p_t.font.size = Pt(13)
            p_t.font.color.rgb = c_dark_text
            p_t.alignment = 1
            
    for i, (_, row) in enumerate(disp.iterrows()):
        for j, col in enumerate(disp.columns):
            cell = table.cell(i+1, j)
            val = str(row[col])
            if col == "행동 지표":
                val = val.split(' ')[0]
            cell.text = val
            for p_t in cell.text_frame.paragraphs:
                p_t.font.size = Pt(12)
                p_t.font.color.rgb = c_dark_text
                p_t.alignment = 1
                if j == 0 or j == cols - 1:
                    p_t.font.bold = True
                if j == cols - 1:
                    if "유의함" in val or "유의" in val:
                        p_t.font.color.rgb = RGBColor(22, 101, 52)
                    else:
                        p_t.font.color.rgb = c_gray

    # ── Slide 4: 핵심 요약 ──
    slide = prs.slides.add_slide(prs.slide_layouts[6])
    txBox = slide.shapes.add_textbox(Inches(1.0), Inches(0.6), Inches(11.3), Inches(1.0))
    p = txBox.text_frame.paragraphs[0]
    p.text = "3. 주요 결론 및 해석 (Conclusion)"
    p.font.size = Pt(24)
    p.font.bold = True
    p.font.color.rgb = c_blue
    
    txBox2 = slide.shapes.add_textbox(Inches(1.0), Inches(1.8), Inches(11.3), Inches(4.5))
    tf2 = txBox2.text_frame
    tf2.word_wrap = True
    
    if lines:
        for line in lines:
            p = tf2.add_paragraph() if tf2.paragraphs[0].text else tf2.paragraphs[0]
            p.text = "• " + line.replace("**", "")
            p.font.size = Pt(16)
            p.font.color.rgb = c_dark_text
            p.space_after = Pt(14)
    else:
        p = tf2.paragraphs[0]
        p.text = "• 통계적 유의수준(p < 0.05) 검정 상으로 선택한 조건 간의 행동 지표 평균값 차이는 일반적인 개체 차 범위 이내에 위치하여 유의한 격차는 검출되지 않았습니다."
        p.font.size = Pt(18)
        p.font.color.rgb = c_dark_text
        p.space_after = Pt(14)
        
    buf = io.BytesIO()
    prs.save(buf)
    buf.seek(0)
    return buf.getvalue()
